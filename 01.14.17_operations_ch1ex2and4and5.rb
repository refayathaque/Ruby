# Modulo (%) gives the remainder of a division operation

puts "Thousands = "
puts 1234/1000
puts "Hundreds = "
puts 1234%1000/100
puts "Tens = "
puts 1234%1000%100/10
puts "Ones = "
puts 1234%1000%100%10

